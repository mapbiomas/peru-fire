def split_multi_band_to_annual(input_multi_band_file, output_per_year_dir, temp_dir):
    print(f"[PROCESSAMENTO] Dividindo {os.path.basename(input_multi_band_file)} em bandas anuais...")
    multi_band_ds = None
    try:
        multi_band_ds = gdal.Open(input_multi_band_file, gdal.GA_ReadOnly)
        if multi_band_ds is None:
            print(f"[ERRO] Não foi possível abrir o arquivo multi-banda para divisão: {input_multi_band_file}")
            return False

        base_name_without_ext = os.path.splitext(os.path.basename(input_multi_band_file))[0]

        for i in range(multi_band_ds.RasterCount):
            band_idx = i + 1
            band_description = multi_band_ds.GetRasterBand(band_idx).GetDescription()
            year = ""
            if band_description and "_" in band_description:
                # Extract year from 'classification_YYYY'
                year = band_description.split('_')[-1]
                if not year.isdigit():
                    year = ""

            # Construct output filename for single band
            single_band_filename = f"{base_name_without_ext}_{year}.tif" if year else f"{base_name_without_ext}_band{band_idx}.tif"
            temp_single_band_path = os.path.join(temp_dir, single_band_filename)
            final_single_band_path = os.path.join(output_per_year_dir, single_band_filename)

            # Check if the single-band file already exists in the per_year folder on Drive
            if os.path.exists(final_single_band_path):
                print(f"  [INFO] Pulando divisão da banda {band_idx} ({final_single_band_path} já existe no Drive).")
                continue

            # Use gdal_translate to extract single band to temp location
            res = subprocess.run([
                "gdal_translate", "-b", str(band_idx),
                "-of", "GTiff",
                "-co", "TILED=YES", "-co", "COMPRESS=LZW", "-co", "PREDICTOR=2",
                "-co", "BIGTIFF=YES", "-co", "SPARSE_OK=TRUE",
                input_multi_band_file, temp_single_band_path
            ], stdout=subprocess.PIPE, stderr=subprocess.PIPE, text=True)

            if res.returncode != 0:
                print(f"  [ERRO] gdal_translate falhou ao extrair banda {band_idx}:", res.stderr.strip())
                continue

            # Set band description explicitly for the single band (good practice)
            if band_description and not set_band_descriptions(temp_single_band_path, [band_description]):
                 print(f"  [ALERTA] Falha ao definir descrição de banda para {temp_single_band_path}")

            # Move single band file from temp to final destination on Drive
            try:
                shutil.move(temp_single_band_path, final_single_band_path)
                print(f"  [MOVENDO BANDA ANUAL PARA DRIVE] {final_single_band_path}")
            except Exception as e:
                print(f"  [ERRO] Falha ao mover {temp_single_band_path} para {final_single_band_path}: {e}")
        return True
    except Exception as e:
        print(f"[ERRO] Falha durante a divisão de bandas para {os.path.basename(input_multi_band_file)}: {e}")
        return False
    finally:
        if multi_band_ds:
            multi_band_ds = None # Close dataset

